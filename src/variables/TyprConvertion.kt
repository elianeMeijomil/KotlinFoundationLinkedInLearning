package variables

fun main(){
    val i1 = 10
    var l1 = i1.toLong()

    println(i1)
    println(l1)

    val d1 = l1.toDouble()
    println(d1)

    var d2 = 25.72
    var i2 = d2.toInt()

    println(d2)
    println(i2)

    var i3 = 10000
    val b1 = i3.toByte()

    println(i3)
    println(b1)
}