package variables

fun main(){
    // Tipor numéricos
    /*
     * Byte     - 8
     * Short    - 16
     * Int      - 32
     * Long     - 64
     * Float    - 32
     * Double   - 64
     */

    var byte : Byte = 8
    var short : Short = 360
    val int: Int = 1000
    val long: Long = 10000
    val float: Float = 150.10F
    val double: Double = 300.00

    println(byte)
    println(short)
    println(int)
    println(long)
    println(float)
    println(double)

    /*
     * Tipo booleano
     * recebe apenas true ou false
     */
    val boolean: Boolean = false
    println(boolean)

    // Tipos textuais
    /*
     * Char - único caracter usando aspas simples
     * String - uma cadeia de caracteres usando aspas duplas
     */

    val char: Char = 'e'
    val string: String = "Eliane foi à feira"

}